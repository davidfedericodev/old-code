      Parkour
Il parkour è considerata una disciplina metropolitana nata in Francia negli anni '80. Il padre fondatore è il francese David Belle che iniziò a praticarlo a Lisse, un sobborgo di Parigi. I praticanti del parkour sono chiamati "traceurs" (creatori di percorsi).
 Le tecniche apprese giocando da bambino nei boschi della campagna francese. Da allora questo, che viene considerato uno "sport", che espone ad alti rischi, ha avuto molto successo nel mondo, soprattutto in Francia e Inghilterra ma anche in Italia. Spesso però viene praticato senza alcuna regola di sicurezza.
Il parkour consiste nell'eseguire un percorso estremo o acrobatico in qualsiasi tipo di luogo, da dietro una palestra come anche su di un tetto di un grattacielo, superando qualsiasi genere di ostacolo con la maggior efficienza, velocità e semplicità di movimento possibile attraverso corsa, salti, equilibrio, scalate, arrampicate, alcuni con vere e proprie figure o volteggi codificati, un po' come accade per altre discipline di acrobazia.
Sebbene molti praticanti neghino che il parkour sia uno sport estremo, ha comunque dei rischi notevoli. Praticare la corsa, saltare, atterrare sul cemento e interagire con una varietà di strutture urbane come tetti, scale, superfici sopraelevate, muri, può provocare infortuni, soprattutto tra chi non si sottopone a un adeguato allenamento. Ma correre rischi eccessivi, mettono in guardia praticanti e associazioni, va contro la filosofia del parkour.

