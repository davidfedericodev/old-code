      Atletica
L’atletica Leggera è uno sport olimpico, nato al tempo degli antichi greci, costituito da svariate discipline, che originano però da 4 movimenti fondamentali per l’uomo: camminare, correre, saltare e lanciare.
L’ Atletica olimpica è costituita da 27 gare suddivise in 5 discipline principali: le corse, la marcia, i salti, i lanci e le prove multiple. Quasi tutte si svolgono all’interno della cosiddetta pista di atletica, tranne la marcia e la maratona che si svolgono su strada.
La corsa è il gesto motorio che caratterizza ben 14 gare del programma Olimpico dell’Atletica, che si svolgono quasi tutte sull’anello di pista, a parte la maratona che avviene su strada.
Le gare di corsa possono suddividersi in 4 categorie:
Sprint: 100, 200 e 400 metri e le staffette 4×100 e 4x400m;
Mezzofondo: 800, 1500, 3000 siepi, 5000 e 10000 metri;
Ostacoli: 100hs (F), 110hs e 400hs;
Fondo: la maratona (42km).
Dal punto di vista tecnico, tattico e fisiologico, e anche psicologico, ogni disciplina di corsa, per quanto il gesto di base sia lo stesso, è estremamente diversa l’una dall’altra.
La marcia altro non è che la camminata agonistica spinta alla massima velocità possibile, senza infrangere la regola principale che obbliga il marciatore a mantenere il contatto tra terreno e piede senza iniziare a correre. Le gare olimpiche di marcia sono di 20km e 50km per gli uomini e di 20km per le donne, si svolgono su strada, a parte il finale (circa 100m) che viene svolto sulla pista di atletica.
I salti si svolgono sulle cosiddette “pedane”, all’interno dell’anello di 400m della pista di atletica.
Si dividono in 2 categorie:
Salti in estensione, dove l’obiettivo è saltare il più lontano possibile
il salto in lungo
il salto triplo
Salti in elevazione, dove l’obiettivo è saltare più alto possibile, superando un’asticella posta ad un’altezza via via più alta durante la competizione:
il salto in alto
il salto con l’asta
In queste 4 gare gli atleti utilizzano le loro doti di velocità, potenza e “leggerezza”.
Anche i lanci, come i salti, si svolgono su delle “pedane”, ed ogni tipologia di lancio ne possiede una specifica ad esso. In queste gare l’obiettivo è lanciare un attrezzo il più lontano possibile, restando con il proprio corpo all’interno della pedana di lancio. I lanci si dividono in 4 tipologie a seconda del tipo di attrezzo:
getto del peso;
lancio del disco;
tiro del giavellotto;
lancio del martello.
Queste gare sono la più grande espressione di potenza dell’atleta tra le gare di atletica leggera.
Le prove multiple sono un insieme di gare olimpiche che vengono svolte da uno stesso atleta in successione, in 2 giornate. Si dividono in:
Decathlon, per gli uomini, una successione di 10 gare diverse.
Eptathlon, per le donne, una successione di 7 gare diverse.

